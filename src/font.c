void drawChar(SDL_Renderer *renderer, int x, int y, char c,
              int scale, Uint8 r, Uint8 g, Uint8 b)
{
    if (c >= 'a' && c <= 'z') c = c - 'a' + 'A';
    int idx = (int)c - 32;
    if (idx < 0 || idx >= 96) idx = 0;

    SDL_SetRenderDrawColor(renderer, r, g, b, 255);
    for (int row = 0; row < FONT_CHAR_H; row++) {
        unsigned char line = GLYPHS[idx][row];
        for (int col = 0; col < FONT_CHAR_W; col++) {
            if (line & (1 << (FONT_CHAR_W - 1 - col))) {
                SDL_Rect px = { x + col*scale, y + row*scale, scale, scale };
                SDL_RenderFillRect(renderer, &px);
            }
        }
    }
}

void drawText(SDL_Renderer *renderer, int x, int y, const char *text,
              int scale, Uint8 r, Uint8 g, Uint8 b)
{
    if (!text) return;
    int step = (FONT_CHAR_W + FONT_SPACING) * scale;
    for (int i = 0; text[i]; i++)
        drawChar(renderer, x + i*step, y, text[i], scale, r, g, b);
}

int textWidth(const char *text, int scale) {
    if (!text || !text[0]) return 0;
    int len = (int)strlen(text);
    return len * (FONT_CHAR_W + FONT_SPACING) * scale - FONT_SPACING * scale;
}

int textHeight(int scale) {
    return FONT_CHAR_H * scale;
}

void drawTextCentered(SDL_Renderer *renderer, int y,
                      int zoneX, int zoneW, const char *text,
                      int scale, Uint8 r, Uint8 g, Uint8 b)
{
    int startX = zoneX + (zoneW - textWidth(text, scale)) / 2;
    drawText(renderer, startX, y, text, scale, r, g, b);
}